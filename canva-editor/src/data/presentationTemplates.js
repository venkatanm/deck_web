const R = (x, y, w, h, fill, extra = {}) => ({
  id: uuidv4(),
  type: 'rect',
  x, y, width: w, height: h, fill,
  rotation: 0, opacity: 1, ...extra,
});

const T = (x, y, w, text, fontSize, fill, extra = {}) => ({
  id: uuidv4(),
  type: 'text',
  x, y, width: w, height: fontSize * 1.4,
  text, fontSize, fill,
  fontFamily: extra.fontFamily || 'Inter',
  fontStyle: extra.bold ? 'bold' : 'normal',
  align: extra.align || 'left',
  rotation: 0, opacity: 1, ...extra,
});

const L = (x, y, w, stroke, extra = {}) => ({
  id: uuidv4(),
  type: 'line',
  x, y, width: w, height: 0, stroke,
  strokeWidth: extra.strokeWidth || 2,
  rotation: 0, opacity: 1, ...extra,
});

